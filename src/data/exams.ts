export function getDaysUntil(dateStr: string | null): number | null {
  if (!dateStr) return null;
  const target = new Date(dateStr);
  const now = new Date();
  const diff = target.getTime() - now.getTime();
  return Math.ceil(diff / (1000 * 60 * 60 * 24));
}

export function getUrgencyLevel(days: number | null): 'urgent' | 'warning' | 'normal' | null {
  if (days === null) return null;
  if (days <= 3) return 'urgent';
  if (days <= 7) return 'warning';
  return 'normal';
}

export function getNextDeadline(exam: Exam): { label: string; date: string; days: number } | null {
  const now = new Date();
  for (const t of exam.timeline) {
    if (t.date && new Date(t.date) > now) {
      const days = getDaysUntil(t.date);
      if (days !== null) return { label: t.label, date: t.date, days };
    }
  }
  return null;
}
